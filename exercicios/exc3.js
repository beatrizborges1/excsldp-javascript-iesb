// = 	Atribuição: atribui um valor à variável
// == Igualdade de valor: verifica se os valores das variáveis são iguais, sem considerar o tipo
// === Igualdade estrita: verifica se os valores e tipos das variáveis são iguais
var x, y, z;
let x = 2;
let y = '2';
let z = 'dois';

console.log('x == y:', x == y); // true (ambos são iguais a "2")
console.log('x === y:', x === y); // false (tipos diferentes)
console.log('x == z:', x == z); // false (tipos diferentes)
console.log('y == z:', y == z); // false (ambos são diferentes.)
console.log('x === z:', x === z); // false (tipos diferentes)
console.log('y === z:', y === z); // false (tipos diferentes)
